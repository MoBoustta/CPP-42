#include "Ice.hpp"

// COPLIEN =====================================================================
Ice::Ice(std::string const & type)
:
	AMateria(
		type
	)
{}

Ice::Ice(const Ice& copy)
:
	AMateria(
		copy
	)
{ *this = copy; }

Ice::~Ice() {}

Ice & Ice::operator=(const Ice& op)
{
	if (this == &op)
		return (*this);
	AMateria::operator=(op);
	return (*this);
}
// =============================================================================

// GETTER - SETTER =============================================================
// =============================================================================

// ADDITIONNAL =================================================================
AMateria*		Ice::clone(void) const { return (new Ice(*this)); }

void			Ice::use(ICharacter& target)
{
	std::cout << "* shoots an ice bolt at " << target.getName() << " *" << std::endl;
	AMateria::use(target);
}
